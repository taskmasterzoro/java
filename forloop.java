class forloop {
 public static void main(String[] args) {
    for(int i=1;i<=5;i++) {   //for loop to iterate from 0 to 5
        System.out.print(i+" ");  //prints the value of 'i' in each iteration
       }   
       int num[]={11,22,33,44,55,66,77,88,99};
       for(int i=0;i< num.length;i++){
        System.out.println(num[i]);//printing array elements using index
       }
       }

}
