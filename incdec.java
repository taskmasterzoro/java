class incdec {
    public static void main (String[] args)
    {
       int number= 5;
       //post increment
       number++;
       System.out.println(number);

       //pre increment
       ++number;
       System.out.println(number);

       //post decrement 
       number--;
       System.out.println(number);

       // pre decrement 
       --number;
       System.out.println(number);
    }
}
