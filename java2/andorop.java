class andorop {
    public static void main(String[] args){
        int age=25;
        /*And operator shows true
         when both condition are true*/
        System.out.println(age>= 18 && age<=80);//true

        /*Or operator shows true when
         one or both condition are true*/
         System.out.println( age<=80 || age>=18);//true

         /*not operator shows true when original
          value was false */
          System.out.println(age!=18);// true 

          //And,Or,Not can be used for boolean data type
    }
}
