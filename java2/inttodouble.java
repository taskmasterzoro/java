class inttodouble {
    public static void main(String[] args){
        // integer to double converse
        int number1=5;
        double number2= number1;
        System.out.println(" double number "+number2);
        /*double to interge converse automaticaally */
        double number3=5.8;
        int number4=(int)number3; 
        /* expility converting the double into integer 
         so we use a cast (int) before the variable name*/
        System.out.println("integer number "+number4);
    }
}
